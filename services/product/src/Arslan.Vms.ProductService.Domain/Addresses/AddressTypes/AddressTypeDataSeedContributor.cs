    public class AddressTypeDataSeedContributor : IDataSeedContributor, ITransientDependency
    {
        private readonly IAddressTypeRepository _addressTreeRepository;
        private readonly IGuidGenerator _guidGenerator;
        private readonly ICurrentTenant _currentTenant;
        List<AddressDataSeed> _address;

        public AddressTypeDataSeedContributor(
            IAddressTypeRepository addressTreeRepository,
            ICurrentTenant currentTenant,
            IGuidGenerator guidGenerator)
        {
            _addressTreeRepository = addressTreeRepository;
            _guidGenerator = guidGenerator;
            _currentTenant = currentTenant;
            _address = new List<AddressDataSeed>();
        }

        [UnitOfWork]
        public virtual async Task SeedAsync(DataSeedContext context)
        {
            var ls = ListAddressTree.TrimEnd(';').Trim('\r').Trim('\n').Split(';');
            var addressModels = new List<AddressDataSeedModel>();

            foreach (var item in ls)
            {
                var x = item.Split(",", StringSplitOptions.RemoveEmptyEntries);
                if (x.Length == 3)
                {
                    addressModels.Add(new AddressDataSeedModel(x[0].Trim('\r').Trim('\n'), x[1], x[2]));
                }
            }

            var data = addressModels.GroupBy(g => new { g.Country, g.City }).Select(s => new { s.Key.Country, s.Key.City, models = s.Select(s => s.District).ToList() });

            foreach (var item in data)
            {
                _address.Add(new AddressDataSeed { Country = item.Country.TrimStart("\r\n".ToCharArray()), City = item.City, Districts = item.models });
            }

            var addressTypes = (await _addressTreeRepository.GetQueryableAsync()).ToList();
            var batchInsert = new List<AddressType>();

            foreach (var _addressType in _address.Select(s => s.Country).Distinct())
            {
                Guid addressTypeId;
                if (!addressTypes.Any(a => a.Name == _addressType && a.Level == 1 && a.ParentId == null))
                {
                    var addressType = new AddressType(_guidGenerator.Create(), _currentTenant.Id, null, _addressType, 1);
                    batchInsert.Add(addressType);
                    addressTypeId = addressType.Id;
                }
                else
                {
                    addressTypeId = addressTypes.FirstOrDefault(a => a.Name == _addressType && a.Level == 1 && a.ParentId == null).Id;
                }


                foreach (var address in _address.Where(w => w.Country == _addressType))
                {
                    Guid brandId;
                    if (!addressTypes.Any(a => a.Name == address.City && a.Level == 2 && a.ParentId == addressTypeId))
                    {
                        var brand = new AddressType(_guidGenerator.Create(), _currentTenant.Id, addressTypeId, address.City, 2);
                        batchInsert.Add(brand);
                        brandId = brand.Id;
                    }
                    else
                    {
                        brandId = addressTypes.FirstOrDefault(a => a.Name == address.City && a.Level == 2 && a.ParentId == addressTypeId).Id;
                    }

                    foreach (var _model in address.Districts)
                    {
                        if (!addressTypes.Any(a => a.Name == _model && a.Level == 3 && a.ParentId == brandId))
                        {
                            var model = new AddressType(_guidGenerator.Create(), _currentTenant.Id, brandId, _model, 3);
                            batchInsert.Add(model);
                        }
                    }
                }
            }

            await _addressTreeRepository.AddRangeAsync(batchInsert);
        }

